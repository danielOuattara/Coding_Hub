// declare variables
class Index {

    public static void main(String[] args) {
        float myNumber = 5.55f;
        System.out.println(myNumber);

        myNumber = myNumber - 1;
        System.out.println(myNumber);

        myNumber--;
        System.out.println(myNumber);

        myNumber -= 1;
        System.out.println(myNumber);
    }
}