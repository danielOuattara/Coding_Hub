// declare variables
class Index {

    public static void main(String[] args) {
        int myNumber = 5;

        myNumber = myNumber - 1;
        System.out.println(myNumber);

        myNumber--;
        System.out.println(myNumber);

        myNumber -= 1;
        System.out.println(myNumber);
    }
}